import pygame

from pygame.locals import(
    K_UP,
    K_DOWN,
    K_LEFT,
    K_RIGHT,
    K_ESCAPE,
    QUIT,
)
pygame.init()
class Game:
    def __init__(self):
        self.speed_up()
        self.pause()
        self.resume()
        self.speed_down()
        self.turn_right
        self.turn_left()

    def start_game_image(self):
        self.start()

    def finish_game_image(self):
        self.exit()


